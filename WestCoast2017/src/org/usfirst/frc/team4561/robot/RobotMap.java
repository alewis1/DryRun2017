package org.usfirst.frc.team4561.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {

	public static final int FRONT_RIGHT_MOTOR_PORT = 0;
	public static final int FRONT_LEFT_MOTOR_PORT = 1;
	public static final int MID_RIGHT_MOTOR_PORT = 2;
	public static final int MID_LEFT_MOTOR_PORT = 3;
	public static final int REAR_RIGHT_MOTOR_PORT = 4;
	public static final int REAR_LEFT_MOTOR_PORT = 5;
	
	public static final int RIGHT_JOYSTICK_PORT = 0;
	public static final int LEFT_JOYSTICK_PORT = 1;
	public static final int CONTROLLER_PORT = 2;
}
